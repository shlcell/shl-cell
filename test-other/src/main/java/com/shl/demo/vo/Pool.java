package com.shl.demo.vo;

import lombok.Data;

@Data
public class Pool {
    private String a;
    private int b;

    public Pool(String a, int b) {
        this.a = a;
        this.b = b;
    }

    public String getA() {
        return a;
    }

    public void setA(String a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
}
